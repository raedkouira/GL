/**
 * Integer literal
 *
 * @author gl28
 * @date 01/01/2020
 */
public class IntLiteral extends AbstractExpr {
    public int getValue() {
        return value;
    }

    private int value;

    public IntLiteral(int value) {
        this.value = value;
    }

    @Override
    public Type verifyExpr(DecacCompiler compiler, EnvironmentExp localEnv,
            ClassDefinition currentClass) throws ContextualError {
    	// decorate **expr** type
    	this.setType(compiler.getEnvTypes().getDefinitionFromName("int").getType());
    	return this.getType();
    }
    
    @Override
	protected void codeExpr(IMAProgram program, int n, RegisterManager registerManager) {
    	registerManager.tryMaxRegisterIndex(n);
    	program.addInstruction(new LOAD(this.dval(), Register.getR(n)));
	}
    
    @Override
	protected DVal dval() {
    	return new ImmediateInteger(value);
	}

    @Override
    String prettyPrintNode() {
        return "Int (" + getValue() + ")";
    }

    @Override
    public void decompile(IndentPrintStream s) {
        s.print(Integer.toString(value));
    }

    @Override
    protected void iterChildren(TreeFunction f) {
        // leaf node => nothing to do
    }

    @Override
    protected void prettyPrintChildren(PrintStream s, String prefix) {
        // leaf node => nothing to do
    }

}
